#--------------------------------------------------------------------------------------------#
# O conversor do tipo float tranforma um inteiro, um float ou um string, representando um 
# da forma que é sintaticamente padrão em um float
#--------------------------------------------------------------------------------------------#

print(float("123.45"))
print(type(float("123.45")))

# O conversor do tipo str transforma os seus argumentos em um string

print(str(20))
print(str(123.45))
print(type(str(123.45))) # Mesmo que não esteja envolvido por "" ele vai mostrar que é um string