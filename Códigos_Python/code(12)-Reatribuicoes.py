#----------------------------------------------------------------------------------
# As vezes nos códigos precisamos reatribuir um resultado diferente a uma variável
# então aqui iremos fazer reatribuições, as variáveis
#----------------------------------------------------------------------------------

# exemplo 01
idade = 20
print(idade)
idade = 30  # reatribuindo o valor da variavel idade
print(idade)

# exemplo 02
a = 5
b = a  # depois desta linha, a e b são iguais
print(a,b)
a = 3  # depois desta linha, a e b não são mais iguais
print(a,b)

# exemplo 03
x = 15 
print(x)
y = x
