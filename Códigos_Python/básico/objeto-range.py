#---------------------------------------------------------------------------
# Em muitas línguagens de programação utilizamos o laço for para realizar 
# iterações, então o python nos fornece o objeto range que simplifica essas
# iterações
#---------------------------------------------------------------------------

# Utilizando range para imprimir uma frase 10 vezes
for i in range(10):
  print("Potatos!")

# Imprimindo número pares de 0 a 10
for i in range(0,10,2):
  print(i)
