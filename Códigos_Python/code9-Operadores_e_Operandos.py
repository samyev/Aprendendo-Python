#-----------------------------------------------------------------------#
# Operadores são símbolos especiais que representam computações como 
# adição, multiplicação e divisão. Os valores sobre os quais o operador 
# trabalha são chamados operandos.
#-----------------------------------------------------------------------#

print("Soma: ", 2 + 3)  # vígula é usada para concatenar/juntar/unir dois tipos de dados em uma mesma frase
print("Subtração: ", 2 - 3)
print("Mutiplicação: ", 2 * 3)
print("Exponenciação: ", 2 ** 3)
print("Exponenciação: ", 3 ** 2)
