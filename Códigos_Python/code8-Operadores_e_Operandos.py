#-----------------------------------------------------------------------#
# Operadores são símbolos especiais que representam computações como 
# adição, multiplicação e divisão. Os valores sobre os quais o operador 
# trabalha são chamados operandos.
#-----------------------------------------------------------------------#

print("Soma: ", 2 + 3)  # vígula é usada para concatenar/juntar/unir dois tipos de dados em uma mesma frase
print("Subtração: ", 2 - 3)
print("Mutiplicação: ", 2 * 3)
print("Exponenciação: ", 2 ** 3)
print("Exponenciação: ", 3 ** 2)

# Python oferece divisão de duas formas diferentes 
print (7 / 4)

# divisão inteira, ele sempre trunca o resultado para o menor inteiro (à esquerda da linha real)
print(7 // 4)
minutos = 645
horas = minutos // 60
print(horas)

# operador módulo também chamado de operador resto da divisão
quociente = 7 // 3 # divisão inteira
print(quociente)
resto = 7 % 3
print(resto)
