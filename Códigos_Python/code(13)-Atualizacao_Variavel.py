#---------------------------------------------------------------------------
# Uma das forma mais comuns de reatribuição é atualização (update) onde o 
# novo valor da variável depende do antigo. 
#---------------------------------------------------------------------------

# Exemplo 01
x = 8
x = x + 1  # recebe o próprio x e atribui mais 1
print(x)

# Exemplo 02
x = 6      # inicialize x
print(x)
x = x + 1  # atualize x
print(x)